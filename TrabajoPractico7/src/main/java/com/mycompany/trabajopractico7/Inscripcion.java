    private void jBInscribirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBInscribirActionPerformed
        // TODO add your handling code here:
        String nombreM = (String)jCBMaterias.getSelectedItem();
        String nombreA = (String)jCBAlumnos.getSelectedItem();
        Collection valores = Menu.materias.values();
        Collection valores2 = Menu.alumnos.values();
        Iterator it = valores.iterator();
        Iterator it2 = valores2.iterator();
        while (it2.hasNext()) {
            Alumno a = (Alumno) it2.next();
            if (nombreA.equals(a.getNombre() + " " + a.getApellido())) {
                while (it.hasNext()) {
                    Materia m1 = (Materia) it.next();
                    if (m1.getNombre().equals(nombreM)) {
                        a.agregarMateria(m1);
                    }
                }
                
            }
        }
    }//GEN-LAST:event_jBInscribirActionPerformed

